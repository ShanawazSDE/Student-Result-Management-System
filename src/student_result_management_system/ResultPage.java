    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
    try {
        // TODO add your handling code here:
        fld_courseName.setText(rs.getString("course_name"));
        fld_branchName.setText(rs.getString("branch_name"));
        fld_rollNo.setText(rs.getString("roll_no"));
        fld_name.setText(rs.getString("name"));
        fld_gender.setText(rs.getString("gender"));
        fld_fatherName.setText(rs.getString("fathers_name"));
        int dsa = Integer.parseInt(rs.getString("DSA"));
        fld_dsa.setText(rs.getString("DSA"));
        int os = Integer.parseInt(rs.getString("OS"));
        fld_os.setText(rs.getString("OS"));
        int dbms = Integer.parseInt(rs.getString("DBMS"));
        fld_dbms.setText(rs.getString("DBMS"));
        int cn = Integer.parseInt(rs.getString("CN"));
        fld_cn.setText(rs.getString("CN"));
        int es = Integer.parseInt(rs.getString("ES"));
        fld_es.setText(rs.getString("ES"));
        fld_total.setText(""+(dsa+os+dbms+cn+es));
        fld_passFail.setText((dsa < 35 || os < 35 || dbms < 35 || cn < 35 || es < 35)? "FAIL" : "PASS");
    } catch (SQLException ex) {
        //Logger.getLogger(ResultPage.class.getName()).log(Level.SEVERE, null, ex);
        JOptionPane.showMessageDialog(null, "Unable to load result");
        
    }
        
    }//GEN-LAST:event_formComponentShown

