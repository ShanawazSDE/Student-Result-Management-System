    private void btnAddResultActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddResultActionPerformed
        // TODO add your handling code here:
        String rollNo = tfRollNo.getText();
        String os = tfOS.getText();
        String dbms = tfDBMS.getText();
        String cn = tfCN.getText();
        String dsa = tfDSA.getText();
        String es = tfES.getText();
        
        connection = DatabaseHelper.getConnection();
        
        try {
            
            if(tfRollNo.getText().equals("")||
                    tfOS.getText().equals("")||
                    tfDBMS.getText().equals("")||
                    tfCN.getText().equals("")||
                    tfDSA.getText().equals("")||
                    tfES.getText().equals("")){
             JOptionPane.showMessageDialog(null, "fill all the fields");
            }else{
                Statement statement = connection.createStatement();
          statement.executeUpdate("insert into result values('"+rollNo+"','"+os+"','"+dbms+"','"+cn+"','"+dsa+"','"+es+"')");
           JOptionPane.showMessageDialog(null, "result inserted successfully");
           tfRollNo.setText("");
           tfOS.setText("");
           tfDBMS.setText("");
           tfCN.setText("");
           tfDSA.setText("");
           tfES.setText("");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Unable to insert result");
        }
        
    }//GEN-LAST:event_btnAddResultActionPerformed

